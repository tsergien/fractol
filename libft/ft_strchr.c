char	*ft_strchr(const char *s, int c)
{
	char	*cs;

	cs = (char *)s;
	while (*cs != 0 && *cs != (char)c)
		cs++;
	if (*cs != '\0' || (char)c == '\0')
		return (cs);
	return (0);
}
