void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	i;
	char			*p;

	i = 0;
	p = s;
	if (!s || !f)
		return ;
	while (*p != '\0')
	{
		f(i, p);
		i++;
		p++;
	}
}
